a = int(5) # a will be 5
b = int(6.7) # b will be 6
c = int("5") # c will be 5
print(a, b, c)

a = float(5) # a will be 5.0
b = float(8.8) # b will be 8.8
c = float("4") # c will be 4.0
print(a, b, c)

a = str("Maksot") # a will be 'Maksot'
b = str(5) # b will be '5'
c = str(3.0) # c will be '3.0'
print(a, b, c)