# Creating variables
a = 18
b = "Maksot"
print(a)
print(b)

c = 18 # c is of type int
c = "Maksot" # c is now of type str
print(a)

# Casting
d = str(5) # d will be '5'
e = int(5) # e will be 5
f = float(5) # f will be 5.0
print(d, e, f)

# Get the type
print(type(a)) 

# Case-sensitive
g = 4
G = "Sally"
print(g)
